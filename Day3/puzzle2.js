window.onload = init;

let aValues = new Array();

function init()
{
    loadValues();
    // loadSampleValues();
    let columns = aValues[0].length;
    let slopes = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]];
    let totalTrees = 1;
    let inputDiv = document.getElementById("input");
    appendToGrid(inputDiv, "Slope");
    appendToGrid(inputDiv, "Trees");
    for (let slopeLoop = 0; slopeLoop < slopes.length; slopeLoop++)
    {
        let right = slopes[slopeLoop][0];
        let down = slopes[slopeLoop][1];
        appendToGrid(inputDiv, String(right) + ", " + String(down));
        let treeCount = 0;
        let column = 0;
        for (let loop1 = down; loop1 < aValues.length; loop1 += down)
        {
            column += right;
            let value = aValues[loop1][column % columns];
            if (value == "#") { treeCount++; }
        }
        appendToGrid(inputDiv, treeCount);
        totalTrees *= treeCount;
    }
    document.getElementById("output").innerText = totalTrees + " is our answer";
}

function appendToGrid(div, string)
{
    let newDiv = document.createElement("div");
    newDiv.innerText = string;
    div.append(newDiv);
}

